	Plane::Plane() : Mesh()
	{
		this->setVertices({
			 -1.0f,  1.0f,  0.0f, 1.0f,
			-1.0f, -1.0f,  0.0f, 0.0f,
			 1.0f, -1.0f,  1.0f, 0.0f,

			-1.0f,  1.0f,  0.0f, 1.0f,
			 1.0f, -1.0f,  1.0f, 0.0f,
			 1.0f,  1.0f,  1.0f, 1.0f
		});

		VertexArray* vao = new VertexArray();

		VertexBufferLayout *vbl = new VertexBufferLayout();
		VertexBuffer* vbo = new VertexBuffer(getVertices().data(), (unsigned int)getVertices().size() * sizeof(float));
		vbl->pushFloat(2);
		vbl->pushFloat(2);
		vao->addBuffer(*vbo, *vbl);

		setVao(vao);
		setVbl(vbl);
		setVbo(vbo);
	}

	Plane::~Plane()
	{
	}

